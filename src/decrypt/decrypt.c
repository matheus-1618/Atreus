typedef unsigned char BYTE;

BYTE *getContentAfterPattern(const char *file_name, const char *pattern) {
    FILE *file = fopen(file_name, "rb");
    if (file == NULL) {
        printf("The file '%s' was not found.\n", file_name);
        return NULL;
    }

    fseek(file, 0, SEEK_END);
    long file_size = ftell(file);
    fseek(file, 0, SEEK_SET);

    BYTE *content = (BYTE *)malloc(file_size);
    if (content == NULL) {
        fclose(file);
        printf("Memory allocation failed.\n");
        return NULL;
    }

    size_t read_bytes = fread(content, 1, file_size, file);
    fclose(file);

    if (read_bytes != (size_t)file_size) {
        free(content);
        printf("Error reading file.\n");
        return NULL;
    }

    size_t pattern_length = strlen(pattern);
    BYTE *last_occurrence = content;
    BYTE *found = content;
    
    while ((found = (BYTE *)strstr((const char *)found, pattern)) != NULL) {
        last_occurrence = found;
        found += pattern_length;
    }

    if (last_occurrence != NULL) {
        size_t content_length = (size_t)file_size - (size_t)(last_occurrence - content) - pattern_length;
        BYTE *output = (BYTE *)malloc(content_length);
        if (output != NULL) {
            memcpy(output, last_occurrence + pattern_length, content_length);
            return output;
        } else {
            printf("Memory allocation failed.\n");
        }
    } else {
        printf("Pattern '%s' not found in the file.\n", pattern);
    }

    free(content);
    return NULL;
}

